    You access the data with the $_REQUEST keyword followed by the name
    of the form field, query parameter, or cookie.

    Note: Because $_REQUEST can combine data from different sources
    (GET, POST, and COOKIE), it can introduce security vulnerabilities
    if not handled carefully. So, while using $_REQUEST can be convenient,
    it is more recommended to use the more specific $_GET, $_POST, and $_COOKIE
    superglobals when possible. */

    
