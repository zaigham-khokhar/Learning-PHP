<?php
    /* Escape Characters  \n - New Line
       \t - Tab
       \\ - Backslash
       \$ - Dollar Sign
       \" - Double Quote
    */  
    
    // Examples of Escape Characters
    // \t - Tab
    echo "Hello\tWorld! <br>";

    // \\ - Backslash
    echo "This is a backslash: \\ <br>";

    // \$ - Dollar Sign
    echo "The price is \$19.99. <br>";

    // \" - Double Quote
    echo "She said, \"Hello, how are you?\" <br>";
?>