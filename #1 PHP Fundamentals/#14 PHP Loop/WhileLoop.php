<?php
    // While Loop in PHP 
    /* While loop is a control structure that allows you to execute a block of code 
    repeatedly as long as a specified condition is true. The syntax for a while loop is 
    as followa:
    while (condition) {
        // code to be executed
    }
    */
    // Example of a while loop that prints the numbers from 1 to 5
    $i = 1;
    while ($i <= 5) {
        echo $i . "\n <br>";
        $i++;
    }

?>