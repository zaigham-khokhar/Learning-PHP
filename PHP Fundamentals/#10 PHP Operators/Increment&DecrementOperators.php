<?php
    // Increment & Decrement Operators
    /* The increment/decrement operators are used to increase or decrease 
    a variable's value by one. */
    // There are Four types of increment/decrement operators:

    // 1. Pre-Increment: ++$variable
    // 2. Post-Increment: $variable++
    // 3. Pre-Decrement: --$variable
    // 4. Post-Decrement: $variable--

    // Example of Pre-Increment
    $a = 5;
    echo "Pre-Increment: " . ++$a . "<br>"; 

    // Example of Post-Increment
    $b = 5;
    echo "Post-Increment: " . $b++ . "<br>";

    // Example of Pre-Decrement
    $c = 5;
    echo "Pre-Decrement: " . --$c . "<br>"; 

    // Example of Post-Decrement
    $d = 5;
    echo "Post-Decrement: " . $d-- . "<br>";

?>